"""Emit cardioseg/RESULTS.json — the ONE canonical source of published numbers (flagship ACDC + Canon,
the nnU-Net baseline, efficiency). `cardioseg/evaluation/sync_numbers.py` renders the doc tables/stats from this,
so a number lives in a single place and the docs can't drift. Regenerate after any eval:

    python -m cardioseg.evaluation.results --run runs/gen

All flagship numbers pool ED+ES (the honest read). nnU-Net numbers are read from
baselines/nnunet/results.json (emitted by baselines/nnunet/score.py --out, ED+ES) — single source,
no hand-copy; refresh by re-running that baseline's score.py.
"""
import argparse
import json
from pathlib import Path

import numpy as np
import polars as pl
import torch

from cardioseg.config import FLAGSHIP_RUN
from cardioseg.data import store
from cardioseg.evaluation.distribution import collect, _pooled, strata_table
from cardioseg.evaluation.evaluate import surface_metrics, CLASSES

ROOT = Path(__file__).resolve().parents[2]  # repo root (…/cardioseg/evaluation/ -> repo)

# nnU-Net v2 (2d, fold0, 50ep) on the SAME split, scored by baselines/nnunet/score.py (ED+ES).
_bj = json.loads((ROOT / "baselines/nnunet/results.json").read_text())
NNUNET = {
    "acdc": {"dice": _bj["acdc"]["dice"], "hd95": _bj["acdc"]["hd95"],
             "ef_mae": _bj["acdc"]["ef_mae"], "ef_bias": _bj["acdc"]["ef_bias"]},
    "canon": {"dice_mean": _bj["canon"]["dice"]["mean"]},
}
# Architectural constants (fvcore single-forward), not a baseline measurement — stay static.
EFFICIENCY = {"ours": {"params": "1.6 M", "flops": "0.8 G"},
              "nnunet": {"params": "92 M", "flops": "19 G"}}
_NAMES = [CLASSES[c][0] for c in CLASSES]  # RV, LV-myo, LV-cav


def _axis(run: Path, device: str, df, with_strata: bool) -> dict:
    rows = collect(run, device, df.iter_rows(named=True))
    dists, dice_acc, ef_gt, ef_pred = _pooled(rows)
    diff = ef_pred - ef_gt
    bias, sd = float(diff.mean()), float(diff.std(ddof=1))
    dice, hd95, assd = {}, {}, {}
    for cl, (name, _) in CLASSES.items():
        pooled = np.concatenate([d for d in dists[cl] if d.size]) if any(d.size for d in dists[cl]) else np.array([])
        m = surface_metrics(pooled) if pooled.size else {"hd95": float("nan"), "assd": float("nan")}
        dice[name] = round(float(np.mean(dice_acc[cl])), 3)
        hd95[name] = round(float(m["hd95"]), 1)
        assd[name] = round(float(m["assd"]), 2)
    out = {"n": len(rows), "dice": {**dice, "mean": round(float(np.mean(list(dice.values()))), 3)},
           "hd95": hd95, "assd": assd, "ef_mae": round(float(np.mean(np.abs(diff))), 1),
           "ef_bias": round(bias, 1), "ef_loa": [round(bias - 1.96 * sd, 1), round(bias + 1.96 * sd, 1)]}
    if with_strata:
        out["strata"] = strata_table(rows, "pathology")
    return out


def build(run: Path) -> dict:
    device = "cuda" if torch.cuda.is_available() else "cpu"
    acdc = store.load(["acdc"]).filter(pl.col("labelled"))
    canon = store.load(["mnms1"]).filter((pl.col("vendor") == "Canon") & pl.col("labelled"))
    return {
        "_note": "Canonical published numbers — generated by cardioseg.evaluation.results; do not hand-edit.",
        "run": run.name,
        "flagship": {"acdc": _axis(run, device, acdc, True), "canon": _axis(run, device, canon, False)},
        "nnunet": NNUNET,
        "efficiency": EFFICIENCY,
    }


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--run", default=FLAGSHIP_RUN)
    ap.add_argument("--out", default="cardioseg/RESULTS.json")
    a = ap.parse_args()
    res = build(Path(a.run))
    Path(a.out).write_text(json.dumps(res, indent=2))
    f = res["flagship"]
    print(f"wrote {a.out}: ACDC mean {f['acdc']['dice']['mean']} / EF {f['acdc']['ef_mae']}% · "
          f"Canon mean {f['canon']['dice']['mean']}")


if __name__ == "__main__":
    main()
